/**
 * @author Edwin
 *
 */
public class CambiarMenuDto {

	private String idPersona;
	private String idTipoPlato;
	private String fechaConsumo;
	/**
	 * @return the idPersona
	 */
	public String getIdPersona() {
		return idPersona;
	}
	/**
	 * @param idPersona the idPersona to set
	 */
	public void setIdPersona(String idPersona) {
		this.idPersona = idPersona;
	}
	/**
	 * @return the idTipoPlato
	 */
	public String getIdTipoPlato() {
		return idTipoPlato;
	}
	/**
	 * @param idTipoPlato the idTipoPlato to set
	 */
	public void setIdTipoPlato(String idTipoPlato) {
		this.idTipoPlato = idTipoPlato;
	}
	/**
	 * @return the fechaConsumo
	 */
	public String getFechaConsumo() {
		return fechaConsumo;
	}
	/**
	 * @param fechaConsumo the fechaConsumo to set
	 */
	public void setFechaConsumo(String fechaConsumo) {
		this.fechaConsumo = fechaConsumo;
	}
	
	
	
}
